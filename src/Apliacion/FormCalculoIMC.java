    private void btnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarActionPerformed
        // TODO add your handling code here:
        zonaMensajes.setText("");
        if(txtAltura.getText().equals("")){
            zonaMensajes.setText("Debe ingresar la altura");
            txtAltura.requestFocus();
        }else{
            if (p.esFloat(txtAltura.getText())){
                float altura = Float.valueOf(txtAltura.getText());
                if (altura > 0){
                    if (txtPeso.getText().equals("")){
                        zonaMensajes.setText("Debe ingresar el peso");
                        txtPeso.requestFocus();
                    }else{
                        if(p.esFloat(txtPeso.getText())){
                            float peso = Float.valueOf(txtPeso.getText());
                            if (peso > 0){
                                // instancia un nuevo obtejo de tipo IMC
                                IMC i = new IMC(altura, peso);
                                
                                // llama a los metodos de la clase IMC
                                txt_imc.setText(String.valueOf(i.obtenerIMC(altura, peso)));
                                txt_clasificacion.setText(String.valueOf(i.obtenerClasificacion()));
                            }else{
                                zonaMensajes.setText("Debe ingresar peso > 0...");
                                txtPeso.setText("");
                                txtPeso.requestFocus();
                            }
                        }
                    }
                }else{
                    zonaMensajes.setText("Debe ingresar altura > 0...");
                    txtAltura.setText("");
                    txtAltura.requestFocus();
                }
            }
        }
    }//GEN-LAST:event_btnConsultarActionPerformed

